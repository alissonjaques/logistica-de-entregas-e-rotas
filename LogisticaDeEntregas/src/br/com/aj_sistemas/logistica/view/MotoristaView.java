    private void preencherTabela() {
        MotoristaController controle = new MotoristaController();
        ArrayList<MotoristaModel> lista = controle.selecionarTodos();
        DefaultTableModel modelo = (DefaultTableModel) getJtMotorista().getModel();
        modelo.setRowCount(0);
        lista.forEach((f) -> {
            modelo.addRow(new Object[]{
                f.getIdMotorista(),
                f.getNome(),
                f.getCnh(),
                f.getIdade()
            });
        });
    }

    public void limparCampos() {
        getJtxIdMotorista().setText("");
        getJtxIdMotorista1().setText("");
        getJtxNome().setText("");
        getJtxCNH().setText("");
        getJtxIdade().setText("");
        getJbEditar().setEnabled(false);
        getJbExcluir().setEnabled(false);
        getJbInserir().setEnabled(true);
    }

    public boolean verificarCampos() {
        if (getJtxIdMotorista().getText().equals("")) {
            return true;
        } else if (getJtxNome().getText().equals("")) {
            return true;
        } else if (getJtxCNH().getText().equals("")) {
            return true;
        } else if (getJtxIdade().getText().equals("")) {
            return true;
        } else {
            return false;
        }
    }

    public boolean verifica() {
        if (getJtxNome().getText().equals("")) {
            return true;
        } else if (getJtxCNH().getText().equals("")) {
            return true;
        } else return getJtxIdade().getText().equals("");
    }

    public void requisitaFoco() {
        if (getJtxIdMotorista().getText().equals("")) {
            getJtxIdMotorista().requestFocus();
        } else if (getJtxNome().getText().equals("")) {
            getJtxNome().requestFocus();
        } else if (getJtxCNH().getText().equals("")) {
            getJtxCNH().requestFocus();
        } else {
            getJtxIdade().requestFocus();
        }
    }

    public boolean verificaId(String idMotorista) {
        if (getJtxIdMotorista().getText().equals("")) {
            return false;
        } else {
            MotoristaController controle = new MotoristaController();
            ArrayList<MotoristaModel> lista = controle.selecionarTodos();
            if (lista.stream().anyMatch((motorista) -> (motorista.getIdMotorista() == Integer.parseInt(idMotorista)))) {
                return true;
            }
        }

        return false;
    }

