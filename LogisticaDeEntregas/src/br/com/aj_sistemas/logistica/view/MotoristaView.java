    private void jbSelecionarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbSelecionarActionPerformed
        if (getJtxIdMotorista().getText().equals("")) {
            JOptionPane.showMessageDialog(this, "Campo vazio, informe um campo válido!");
            getJtxIdMotorista().requestFocus();
        } else {
            MotoristaModel motorista = new MotoristaModel();
            motorista.setIdMotorista(Integer.parseInt(getJtxIdMotorista().getText()));

            MotoristaController controle = new MotoristaController();
            motorista = controle.selecionar(motorista);
            if (motorista != null) {
                getJtxIdMotorista().setText(String.valueOf(motorista.getIdMotorista()));
                getJtxIdMotorista1().setText(String.valueOf(motorista.getIdMotorista()));
                getJtxCNH().setText(motorista.getCnh());
                getJtxNome().setText(motorista.getNome());
                getJtxIdade().setText(String.valueOf(motorista.getIdade()));
                //getJbInserir().setEnabled(false);
                getJbEditar().setEnabled(true);
                getJbExcluir().setEnabled(true);
                getJbInserir().setEnabled(false);
            } else {
                JOptionPane.showMessageDialog(this, "ID Não encontrado!");
            }

        }
    }//GEN-LAST:event_jbSelecionarActionPerformed

